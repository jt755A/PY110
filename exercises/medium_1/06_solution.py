def is_prime(integer):
    if integer == 1:
        return False

    consecutive_integers = range(1, integer + 1)

    divisors = {num for num in consecutive_integers if integer % num == 0}
    prime_divisors = {1, integer}

    final_divisors = divisors - prime_divisors
    return not bool(final_divisors)

